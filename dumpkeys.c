    warcraft3_strong_public_rsa->e = BN_bin2bn(kBlizzardStrongPublicExponent, sizeof(kBlizzardStrongPublicExponent), warcraft3_strong_public_rsa->e);
    
    // Prepare WoW's strong RSA public key
